#pragma once
#include <string>

/**
 * Converts each character in a c-string to uppercase.
 *
 * @param str The c-string to be capitalized.
 */
void toUppercase(char* str);

/**
 * Converts each character in a c-string to lowercase.
 *
 * @param str The c-string whose character is to be converted to lowercase.
 */
void toLowercase(char* str);

/**
 * Reverses the case of characters of a c-string i.e. uppercase characters are converted to lowercase and vice versa.
 *
 * @param str The c-string whose characters' case is to be reversed.
 */
void reverseCase(char* str);
