#pragma once
#include <string>

/**
 * Converts each character in a string to uppercase.
 *
 * @param str The string to be capitalized.
 */
void capitalize(std::string* str);

/**
 * Converts each character in a c-string to lowercase.
 *
 * @param str The string whose character is to be converted to lowercase.
 */
void toLowercase(std::string* str);

/**
 * Reverses the characters of a c-string.
 *
 * @param str The string whose characters are to be reversed.
 */
void reverseString(std::string* str);

